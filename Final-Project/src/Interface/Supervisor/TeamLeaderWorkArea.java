    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow=jTable1.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row");
        }
        
        else{
            Person p=(Person)personCombo.getSelectedItem();
            Supervisor s=p.getMySupervisor();
            WorkProgress wp=teamLeaderOrganization.getProgressHistory().findWorkProgressbyDateAndPerson((Date)jTable1.getValueAt(selectedRow, 3), p);
            s.getProgressReport().add(wp);
            //s.getProgressReport().
            JOptionPane.showMessageDialog(null, "Progress sent to Supervisor");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


