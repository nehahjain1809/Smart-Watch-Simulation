    public void LoadPersonCombo(){
        personCombo.removeAll();
        for(Person per:t.getPersonList()){
            personCombo.addItem(per);
            
        }
    }
    public void MessageTable(){
        DefaultTableModel dtm =(DefaultTableModel)tblMessages.getModel();
        dtm.setRowCount(0);
        for (Message m:t.getMsgList())
            
        {
            Object row[]= new Object [1];
            row[0]=m.getMessage();
            dtm.addRow(row);
        }
    }
    public void populateTable(Person p){
        DefaultTableModel dtm =(DefaultTableModel)jTable1.getModel();
        dtm.setRowCount(0);
        for (WorkProgress wp:p.getMyWorkProgress())
        //for(int i=0;i<(getBestStartDates.size());i++)    
        {
            Object row[]= new Object [4];
            row[0]=p.toString();
            row[1]=wp.getProgress();
            row[2]=wp.getSupervisor().getSupervisorName();
            row[3]=wp.getDate();
            dtm.addRow(row);
        }
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(
                Integer.parseInt(percentWorkProgressTxt.getText())<0
                || percentWorkProgressTxt.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Please select or enter valid values");
        }
        else{
        Person p=(Person)(personCombo.getSelectedItem());
        Supervisor s=p.getMySupervisor();
        Date date=dateChooser.getDate();
        WorkProgress wp=teamLeaderOrganization.getProgressHistory().addWorkProgress();
        wp.setP(p);
        wp.setSupervisor(s);
        wp.setDate(date);
        wp.setProgress(Integer.parseInt(percentWorkProgressTxt.getText()));
        p.getMyWorkProgress().add(wp);
        JOptionPane.showMessageDialog(null, "Progress added successfully.");
        //s.getProgressReport().add(wp);
        populateTable(p);
        
        }
                
    }//GEN-LAST:event_btnSaveActionPerformed

